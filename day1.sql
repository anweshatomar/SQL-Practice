Day 1- Write a query identifying the type of each record in the TRIANGLES table using its three side lengths. Output one of the following statements for each record in the table:
Equilateral: It's a triangle with  3 sides of equal length.
Isosceles: It's a triangle with  2 sides of equal length.
Scalene: It's a triangle with  3 sides of differing lengths.
Not A Triangle: The given values of A, B, and C don't form a triangle.
This problem can be solved by using the CASE statement, the CASE statement goes 
through conditions and returns a value when the first condition is met, 
it is similar to an if-then-else statement. Once the condition is true it 
will stop and return the result. If none of the conditions are true it returns a value in the else clause.
